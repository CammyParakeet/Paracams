package com.parakeetstudios.paracams.api.utils;

/**
 * The axis in the camera view plane
 */
public enum ViewAxis {

    /**
     * The x axis.
     * AKA: left and right
     */
    X,
    /**
     * The y axis.
     * AKA: up and down
     */
    Y,
    /**
     * The z axis.
     * AKA: back and forth
     */
    Z;

}
